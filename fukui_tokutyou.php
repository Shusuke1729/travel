  <H2>気候</H2>
  福井県は、冬に晴れの日が少なく、降水量が多い特徴を持つ日本海岸気候区に属します。特に嶺北地方の奥越では気温は低く、<font color = "orange">冬の雪が多い</font>特徴があり、平野部でも大雪となることがあります。<br>嶺南地方は、嶺北地方と比べると、雪雲の影響を受けにくく、<font color = "green">冬の降水量は少なめで、比較的あたたかい気候</font>となっています。
