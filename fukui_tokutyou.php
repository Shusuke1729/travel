  <IMG SRC = "2539_d006_l.jpg"><br>
  敦賀市北東の木の芽峠を境として、北～北東部の「<font color = "ORANGE">嶺北地域</font>」、若狭湾に沿った南西部の「<font color = "GREEN">嶺南地域</font>」に分けられます。<br>嶺北と嶺南では同じ県にもかかわらず、文化が大きく異なります。例えば、方言でいうと嶺北は「抑揚がなく、平板なしゃべり方」、嶺南は「京都に近いしゃべり方」であり、ちょっと聞くだけでも違いがわかります。
